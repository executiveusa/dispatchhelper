// Helper function to get color value
export const getColor = (path: string): string => {
  const parts = path.split('.');
  let value: any = theme.colors;

  for (const part of parts) {
    value = value[part];
    if (value === undefined) {
      console.warn(`Color path "${path}" not found in theme`);
      return '#000000';
    }
  }

  return value;
};

